export function buildGraphVisualMapRuntimeDeps(host = {}) {
  const {
    GRAPH_RELATION_GROUP_META = {},
    GRAPH_RELATION_MARKER_COLORS = {},
    GRAPH_VISUAL_ZOOM_OPTIONS = {},
    escapeHtml = (value) => String(value ?? ""),
    graphBuildReadingLensState = () => ({ active: false }),
    graphBuildVisualLayout = (nodes = [], edges = []) => ({ nodes, edges, width: 960, height: 520, nodeMap: new Map(), clusterMeta: [] }),
    graphClusterGlow = () => "",
    graphDenseGalaxyMode = () => false,
    graphEdgePath = () => "",
    graphEdgeSelectionKey = () => "",
    graphEdgeShouldRender = () => true,
    graphEdgeVisibleAtFit = () => true,
    graphFocusContextPanel = () => "",
    graphFocusDepthMeta = (value) => ({ key: value || "1" }),
    graphIcon = () => "",
    graphNebulaField = () => "",
    graphNodeAttentionReasons = () => [],
    graphNodeClass = () => "",
    graphNodeNeedsRelationWorkflow = () => false,
    graphNodeShowsAsPoint = () => false,
    graphNodeStarRank = () => 0,
    graphReadingLensControls = () => "",
    graphReadingLensMeta = (value) => ({ key: value || "overview" }),
    graphReadingModeMeta = (value) => ({ key: value || "argument" }),
    graphRelationGroupMeta = () => ({}),
    graphRelationSourceLabel = () => "",
    graphRelationTypeFilter = () => "",
    graphRelationTypeLabel = () => "",
    graphRelationVisual = () => ({ key: "neutral" }),
    graphResearchNavigatorEntry = () => "",
    graphResearchNavigatorPanel = () => "",
    graphSelectionPanel = () => "",
    graphShortTitle = (node) => node?.title || node?.id || "",
    graphStarfield = () => "",
    graphState = {},
    graphThemeBoundary = () => "",
    graphThemeBoundaryMeta = () => null,
    graphViewModeForRelationType = (value) => value || "argument",
    graphViewModeSwitcher = () => "",
    graphZoomOption = (value) => ({ key: value || "fit", scale: 1 }),
    normalizeGraphSelectionForVisibleItems = (selection) => selection,
    noteTypeLabel = () => "",
    shouldShowGraphDensityHint = () => false
  } = host;

  return {
    graphState,
    graphFocusDepthMeta,
    graphReadingModeMeta,
    graphViewModeForRelationType,
    graphBuildVisualLayout,
    graphZoomOption,
    graphReadingLensMeta,
    graphEdgePath,
    graphRelationVisual,
    graphDenseGalaxyMode,
    shouldShowGraphDensityHint,
    normalizeGraphSelectionForVisibleItems,
    graphNodeNeedsRelationWorkflow,
    graphBuildReadingLensState,
    zoomOptions: GRAPH_VISUAL_ZOOM_OPTIONS,
    relationGroupMeta: GRAPH_RELATION_GROUP_META,
    renderGraphRelationTypeFilter: graphRelationTypeFilter,
    graphThemeBoundaryMeta,
    renderGraphThemeBoundary: graphThemeBoundary,
    renderGraphStarfield: graphStarfield,
    renderGraphNebulaField: graphNebulaField,
    renderGraphClusterGlow: graphClusterGlow,
    renderGraphFocusContextPanel: graphFocusContextPanel,
    renderGraphSelectionPanel: graphSelectionPanel,
    renderGraphResearchNavigatorPanel: graphResearchNavigatorPanel,
    renderGraphResearchNavigatorEntry: graphResearchNavigatorEntry,
    escapeHtml,
    renderGraphIcon: graphIcon,
    renderGraphViewModeSwitcher: graphViewModeSwitcher,
    renderGraphReadingLensControls: graphReadingLensControls,
    markerColors: GRAPH_RELATION_MARKER_COLORS,
    graphNodeClass,
    graphNodeStarRank,
    graphShortTitle,
    noteTypeLabel,
    graphNodeAttentionReasons,
    graphNodeShowsAsPoint,
    graphRelationTypeLabel,
    graphRelationSourceLabel,
    graphRelationGroupMeta,
    graphEdgeSelectionKey,
    graphEdgeVisibleAtFit,
    graphEdgeShouldRender
  };
}
